  int a = 10, b = 3;

  print(a + b);  // 13
  print(a - b);  // 7
  print(a * b);  // 30
  print(a / b);  // 3.333 (double)
  print(a ~/ b); // 3 (integer division)
  print(a % b);  // 1

  // Comparison
  print(a > b);    // true
  print(a == b);   // false

